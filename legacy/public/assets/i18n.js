let LANGUE = localStorage.getItem('an_langue') || 'fr';

/** Traduit une clé dans la langue active (repli : français, puis clé). */
function t(cle) {
  return I18N[LANGUE]?.[cle] ?? I18N.fr[cle] ?? cle;
}

/** Applique langue + direction au document. */
function appliquerLangue() {
  document.documentElement.lang = LANGUE;
  document.documentElement.dir = LANGUE === 'ar' ? 'rtl' : 'ltr';
  document.title = LANGUE === 'ar'
    ? 'الجمعية الوطنية — جمهورية تشاد'
    : 'Assemblée Nationale — République du Tchad';
}

function basculerLangue() {
  LANGUE = LANGUE === 'fr' ? 'ar' : 'fr';
  localStorage.setItem('an_langue', LANGUE);
  appliquerLangue();
}
